public class InchesToFeet45 {
    public static void main (String [] args){
        int value = 45;
        int feet = value / 12;
        int inches = value % 12;
        System.out.print("45 inches = ");
        System.out.print(feet);
        System.out.print(" feet, ");
        System.out.print(inches);
        System.out.println(" inches");
    }
}